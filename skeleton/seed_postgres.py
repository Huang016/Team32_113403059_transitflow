def seed_metro_schedule_stops(
    cur,
    schedules: list[dict[str, Any]],
    schedule_ids: dict[str, int],
    metro_station_ids: dict[str, int],
) -> int:
    columns = [
        "schedule_id",
        "stop_order",
        "station_id",
        "travel_time_from_origin_min",
    ]

    rows = []
    for schedule in schedules:
        schedule_code = schedule.get("schedule_id")
        schedule_pk = require_mapping(schedule_ids, schedule_code, "metro schedule_code")

        for idx, station_code in enumerate(schedule.get("stops_in_order", []), start=1):
            rows.append(
                (
                    schedule_pk,
                    idx,
                    require_mapping(metro_station_ids, station_code, "metro schedule stop station_id"),
                    travel_time_for_stop(schedule, station_code),
                )
            )

    count = insert_many(cur, "metro_schedule_stops", columns, rows)
    print(f"  metro_schedule_stops: {count}")
    return count


