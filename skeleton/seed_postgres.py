# ── main ─────────────────────────────────────────────────────────────────────


def main() -> None:
    print("Connecting to PostgreSQL...")
    conn = connect()
    conn.autocommit = False
    cur = conn.cursor()

    try:
        print("Seeding tables (dependency order):")

        # 1. Independent master data.
        user_ids = seed_users(cur)
        metro_station_ids = seed_metro_stations(cur)
        rail_station_ids = seed_national_rail_stations(cur)
        update_station_interchanges(cur)

        # 2. Schedules and normalized schedule stops.
        metro_schedule_ids = seed_metro_schedules(cur, metro_station_ids)
        rail_schedule_ids = seed_national_rail_schedules(cur, rail_station_ids)

        # 3. Schedule children.
        seed_metro_platforms(cur, metro_schedule_ids, metro_station_ids)
        seed_national_rail_platforms(cur, rail_schedule_ids, rail_station_ids)
        rail_seat_ids = seed_national_rail_seats(cur, rail_schedule_ids)

        # 4. Users' transactions.
        monthly_pass_ids = seed_metro_monthly_passes(cur, user_ids)
        seed_metro_monthly_pass_payments(cur, monthly_pass_ids)
        booking_ids = seed_national_rail_bookings(
            cur,
            user_ids,
            rail_schedule_ids,
            rail_station_ids,
            rail_seat_ids,
        )
        trip_ids = seed_metro_trips(
            cur,
            user_ids,
            metro_schedule_ids,
            metro_station_ids,
            monthly_pass_ids,
        )

        # 5. Split payments and feedback.
        seed_payments(cur, booking_ids, trip_ids)
        seed_feedback(cur, user_ids, booking_ids, trip_ids)
        update_loyalty_points(cur)

        print_summary(cur)

        conn.commit()
        print("\nAll done. Database seeded successfully.")

    except Exception as e:
        conn.rollback()
        print(f"\nError: {e}")
        raise

    finally:
        cur.close()
        conn.close()


if __name__ == "__main__":
    main()
