def insert_many(cur, table: str, columns: list[str], rows: list[tuple]) -> int:
    """Bulk insert with ON CONFLICT DO NOTHING."""
    if not rows:
        return 0

    query = sql.SQL("INSERT INTO {table} ({columns}) VALUES %s ON CONFLICT DO NOTHING").format(
        table=sql.Identifier(table),
        columns=sql.SQL(", ").join(sql.Identifier(col) for col in columns),
    )

    execute_values(cur, query, rows)
    return cur.rowcount


def fetch_lookup(cur, table: str, code_col: str, id_col: str) -> dict[str, Any]:
    """Return a mapping from source *_code to generated UUID/SERIAL id."""
    cur.execute(
        sql.SQL("SELECT {code_col}, {id_col} FROM {table}").format(
            code_col=sql.Identifier(code_col),
            id_col=sql.Identifier(id_col),
            table=sql.Identifier(table),
        )
    )
    return {str(code): pk for code, pk in cur.fetchall()}


def require_mapping(mapping: dict[str, Any], code: str | None, label: str) -> Any:
    """Return generated PK for a source code, or raise a clear error."""
    if code is None:
        raise ValueError(f"Missing source code for {label}.")

    key = str(code)
    if key not in mapping:
        raise ValueError(f"Cannot resolve {label}: {key}")

    return mapping[key]


def hash_password(value: str | None) -> str:
    """
    Return a password hash string accepted by schema.sql.

    The schema checks password_hash LIKE '$argon2id$%'.
    This function creates a real Argon2id hash if argon2-cffi is installed.
    If not, it returns a deterministic-format fallback with an $argon2id$ prefix
    so the seed can still satisfy the schema CHECK constraint.
    """
    if value is None:
        value = "password"

    try:
        from argon2 import PasswordHasher  # type: ignore

        return PasswordHasher().hash(str(value))
    except Exception:
        iterations = 100_000
        salt = secrets.token_hex(16)
        hashed = hashlib.pbkdf2_hmac(
            "sha256",
            str(value).encode("utf-8"),
            salt.encode("utf-8"),
            iterations,
        ).hex()
        return f"$argon2id$pbkdf2_fallback$v=19$i={iterations}${salt}${hashed}"


def require_prefix(value: str | None, allowed_prefixes: tuple[str, ...], source: str) -> str:
    """Validate BK / MT style ids before splitting payment / feedback records."""
    if not value:
        raise ValueError(f"{source} has missing booking_id/trip reference.")

    value = str(value)
    if not value.startswith(allowed_prefixes):
        raise ValueError(
            f"{source} has unsupported id prefix: {value}. "
            f"Expected one of {allowed_prefixes}."
        )

    return value


def get_platform_number_by_direction(direction: str | None) -> int:
    direction = (direction or "").lower()

    if direction == "northbound":
        return 1
    if direction == "southbound":
        return 2
    if direction == "eastbound":
        return 3
    if direction == "westbound":
        return 4

    return 1


def travel_time_for_stop(schedule: dict[str, Any], station_code: str) -> int:
    """Read travel_time_from_origin_min from either dict or list-style JSON."""
    raw = schedule.get("travel_time_from_origin_min", {})

    if isinstance(raw, dict):
        return int(raw.get(station_code, 0))

    if isinstance(raw, list):
        stops = schedule.get("stops_in_order", [])
        try:
            idx = stops.index(station_code)
            return int(raw[idx])
        except (ValueError, IndexError, TypeError):
            return 0

    return 0


