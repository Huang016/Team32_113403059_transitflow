def seed_metro_monthly_pass_payments(
    cur,
    monthly_pass_ids: dict[str, Any],
) -> int:
    """
    Seed optional metro monthly pass payment data when a JSON file exists.

    Supported filenames:
      - metro_monthly_pass_payments.json
      - monthly_pass_payments.json

    Expected JSON fields:
      - payment_id: source payment code, e.g. MPP001
      - pass_id / monthly_pass_id / metro_monthly_pass_id: source monthly pass code
      - amount_usd, method, status, paid_at
    """
    data = load_optional("metro_monthly_pass_payments.json") or load_optional("monthly_pass_payments.json")

    if not data:
        print("  metro_monthly_pass_payments: 0 (optional file not found)")
