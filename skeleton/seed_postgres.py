def update_loyalty_points(cur) -> None:
    """
    Recalculate loyalty points from paid rail, metro trip, and monthly pass transactions.

    Rule used here:
      points = FLOOR(amount_usd * 10) for each paid transaction
      only completed/confirmed rail bookings, completed metro trips,
      and paid monthly pass purchases count
    """
    cur.execute("""
        UPDATE registered_users
        SET loyalty_points = 0;
    """)

    cur.execute("""
        UPDATE registered_users u
        SET loyalty_points = p.points
        FROM (
            SELECT
                user_id,
                SUM(points)::int AS points
            FROM (
                SELECT
                    b.user_id,
                    FLOOR(np.amount_usd * 10)::int AS points
                FROM national_rail_bookings b
                JOIN national_rail_payments np
                  ON np.booking_id = b.booking_id
                WHERE b.status IN ('confirmed', 'completed')
                  AND np.status = 'paid'

                UNION ALL

                SELECT
                    t.user_id,
                    FLOOR(mp.amount_usd * 10)::int AS points
                FROM metro_trips t
                JOIN metro_payments mp
                  ON mp.trip_id = t.trip_id
                WHERE t.status = 'completed'
                  AND mp.status = 'paid'

                UNION ALL

                SELECT
                    p.user_id,
                    FLOOR(mpp.amount_usd * 10)::int AS points
                FROM metro_monthly_passes p
                JOIN metro_monthly_pass_payments mpp
                  ON mpp.pass_id = p.pass_id
                WHERE mpp.status = 'paid'
            ) all_points
            GROUP BY user_id
        ) p
        WHERE u.user_id = p.user_id;
    """)
    print("  loyalty_points recalculated")


