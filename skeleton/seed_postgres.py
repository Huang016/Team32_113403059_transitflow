def load(filename: str) -> list[dict[str, Any]]:
    """Load a required JSON file from train-mock-data/."""
    path = os.path.join(DATA_DIR, filename)

    if not os.path.exists(path):
        raise FileNotFoundError(f"Data file not found: {path}")

    with open(path, encoding="utf-8") as f:
        data = json.load(f)

    if not isinstance(data, list):
        raise ValueError(f"{filename} must contain a JSON array/list.")

    return data


def load_optional(filename: str) -> list[dict[str, Any]]:
    """Load an optional JSON file. Missing optional files return an empty list."""
    path = os.path.join(DATA_DIR, filename)

    if not os.path.exists(path):
        return []

    with open(path, encoding="utf-8") as f:
        data = json.load(f)

    if not isinstance(data, list):
        raise ValueError(f"{filename} must contain a JSON array/list.")

    return data


