def execute_booking(
    user_id: str,
    schedule_id: str,
    origin_station_id: str,
    destination_station_id: str,
    travel_date: str,
    fare_class: str,
    seat_id: str,
    ticket_type: str = "single",
) -> tuple[bool, dict | str]:
    """Create a national rail booking and matching national_rail_payments record."""
    conn = psycopg2.connect(PG_DSN)
    conn.autocommit = False

    try:
        with conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor) as cur:
            cur.execute(
                "SELECT user_id FROM registered_users WHERE user_id = %s AND is_active = TRUE;",
                (user_id,),
            )
            if not cur.fetchone():
                conn.rollback()
                return False, "User does not exist or is inactive."

            cur.execute(
                """
                SELECT
                    s.schedule_id,
                    s.stops_in_order,
                    s.first_train_time,
                    s.fare_classes,
                    origin_pos.ord::int AS origin_pos,
                    destination_pos.ord::int AS destination_pos
                FROM national_rail_schedules s
                JOIN LATERAL (
                    SELECT x.ord
                    FROM jsonb_array_elements_text(s.stops_in_order) WITH ORDINALITY AS x(station_id, ord)
                    WHERE x.station_id = %s
                    LIMIT 1
                ) AS origin_pos ON TRUE
                JOIN LATERAL (
                    SELECT x.ord
                    FROM jsonb_array_elements_text(s.stops_in_order) WITH ORDINALITY AS x(station_id, ord)
                    WHERE x.station_id = %s
                    LIMIT 1
                ) AS destination_pos ON TRUE
                WHERE s.schedule_id = %s
                  AND origin_pos.ord < destination_pos.ord;
                """,
                (origin_station_id, destination_station_id, schedule_id),
            )
            schedule = cur.fetchone()
            if not schedule:
                conn.rollback()
                return False, "No valid national rail schedule found for this route."

            stops_travelled = int(schedule["destination_pos"]) - int(schedule["origin_pos"])
            departure_time = schedule["first_train_time"]

            fare = query_national_rail_fare(schedule_id, fare_class, stops_travelled)
            if not fare:
                conn.rollback()
                return False, "Invalid fare class or schedule."

            available = query_available_seats(
                schedule_id=schedule_id,
                travel_date=travel_date,
                fare_class=fare_class,
                departure_time=str(departure_time),
            )
            if not available:
                conn.rollback()
                return False, "No available seats for this schedule, date, and fare class."

            if seat_id.lower() == "any":
                selected_seat = available[0]
            else:
                selected_seat = next((seat for seat in available if seat["seat_id"] == seat_id), None)

            if not selected_seat:
                conn.rollback()
                return False, f"Seat {seat_id} is not available."

            booking_id = _gen_booking_id(cur)
            payment_id = _gen_payment_id(cur)
            now = datetime.now(timezone.utc)

            cur.execute(
                """
                INSERT INTO national_rail_bookings (
                    booking_id,
                    user_id,
                    schedule_id,
                    origin_station_id,
                    destination_station_id,
                    travel_date,
                    departure_time,
                    ticket_type,
                    fare_class,
                    coach,
                    seat_id,
                    stops_travelled,
                    amount_usd,
                    status,
                    booked_at,
                    travelled_at
                )
                VALUES (
                    %s, %s, %s, %s, %s,
                    %s, %s, %s, %s, %s,
                    %s, %s, %s, %s, %s, NULL
                )
                RETURNING *;
                """,
                (
                    booking_id,
                    user_id,
                    schedule_id,
                    origin_station_id,
                    destination_station_id,
                    travel_date,
                    departure_time,
                    ticket_type,
                    fare_class,
                    selected_seat["coach"],
                    selected_seat["seat_id"],
                    stops_travelled,
                    fare["total_fare_usd"],
                    "confirmed",
                    now,
                ),
            )
            booking = dict(cur.fetchone())

            cur.execute(
                """
                INSERT INTO national_rail_payments (
                    payment_id,
                    booking_id,
                    amount_usd,
                    method,
                    status,
                    paid_at
                )
                VALUES (%s, %s, %s, %s, %s, %s)
                RETURNING *;
                """,
                (
                    payment_id,
                    booking_id,
                    fare["total_fare_usd"],
                    "credit_card",
                    "paid",
                    now,
                ),
            )
            payment = dict(cur.fetchone())

            points_earned = int(float(payment["amount_usd"]) * 10)

            cur.execute(
                """
                UPDATE registered_users
                SET loyalty_points = loyalty_points + %s
                WHERE user_id = %s
                RETURNING loyalty_points;
                """,
                (points_earned, user_id),
            )

            current_loyalty_points = cur.fetchone()["loyalty_points"]
            conn.commit()
            return True, {"booking": booking, "payment": payment , "loyalty_points": current_loyalty_points}

    except Exception as e:
        conn.rollback()
        return False, str(e)

    finally:
        conn.close()


def execute_cancellation(booking_id: str, user_id: str) -> tuple[bool, dict | str]:
    """Cancel a national rail booking owned by the given user."""
    conn = psycopg2.connect(PG_DSN)
    conn.autocommit = False

    try:
        with conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor) as cur:
            cur.execute(
                """
                SELECT
                    b.*,
                    nrs.service_type
                FROM national_rail_bookings b
                JOIN national_rail_schedules nrs ON b.schedule_id = nrs.schedule_id
                WHERE b.booking_id = %s
                  AND b.user_id = %s;
                """,
                (booking_id, user_id),
            )
            booking = cur.fetchone()

            if not booking:
                conn.rollback()
                return False, "Booking not found or does not belong to this user."

            if booking["status"] == "cancelled":
                conn.rollback()
                return False, "Booking is already cancelled."

            travel_datetime = datetime.combine(
                booking["travel_date"],
                booking["departure_time"] or time.min,
            ).replace(tzinfo=timezone.utc)

            now = datetime.now(timezone.utc)
            hours_before = (travel_datetime - now).total_seconds() / 3600

            service_type = booking["service_type"]
            if service_type == "express":
                if hours_before >= 48:
                    refund_rate = 1.0
                elif hours_before >= 24:
                    refund_rate = 0.5
                else:
                    refund_rate = 0.0
                policy_note = "Express service refund policy applied."
            else:
                if hours_before >= 48:
                    refund_rate = 1.0
                elif hours_before >= 24:
                    refund_rate = 0.75
                elif hours_before >= 2:
                    refund_rate = 0.5
                else:
                    refund_rate = 0.0
                policy_note = "Normal service refund policy applied."

            refund_amount = round(float(booking["amount_usd"]) * refund_rate, 2)

            cur.execute(
                """
                UPDATE national_rail_bookings
                SET status = 'cancelled'
                WHERE booking_id = %s
                RETURNING *;
                """,
                (booking_id,),
            )
            updated_booking = dict(cur.fetchone())

            cur.execute(
                """
                UPDATE national_rail_payments
                SET status = 'refunded'
                WHERE booking_id = %s
                RETURNING *;
                """,
                (booking_id,),
            )
            payment = cur.fetchone()

            conn.commit()

            return True, {
                "booking": updated_booking,
                "payment": dict(payment) if payment else None,
                "refund_rate": refund_rate,
                "refund_amount_usd": refund_amount,
                "policy_note": policy_note,
            }

    except Exception as e:
        conn.rollback()
        return False, str(e)

    finally:
        conn.close()


